def _attn_smoothing(graph: dgl.DGLGraph, *args) -> torch.Tensor:
    feat = args[0]
    # num_heads = args[1]
    with graph.local_scope():
        graph.ndata['ft'] = feat
        graph.update_all(fn.u_mul_e('ft', 'a', 'm'),
                         fn.sum('m', 'tmp'))
        tmp = graph.ndata['tmp']
        graph.update_all(fn.copy_e('a', 'm'),
                         fn.sum('m', 'd'))
        tmp2 = graph.ndata['d'] * feat
    tr = torch.einsum('ibj,ibj->b', feat, tmp2 - tmp)
    loss = tr.mean() / graph.num_nodes()
    return loss


def _attn_s_smoothing(graph: dgl.DGLGraph, *args) -> torch.Tensor:
    compl_graph: dgl.DGLGraph = args[1]
    with compl_graph.local_scope():
        compl_graph.ndata['ft'] = graph.ndata['ft']
        compl_graph.update_all(fn.u_mul_e('ft', 'w', 'm'),
                         fn.sum('m', 'tmp'))
        tmp = compl_graph.ndata['tmp']
        trace = torch.einsum('ibj,ibj->b', graph.ndata['ft'], tmp)
        return trace.sum() / graph.num_nodes()


def _attn_s_smoothing_ortho(graph: dgl.DGLGraph, *args) -> torch.Tensor:
    z: torch.Tensor = graph.ndata['ft']
    dim = z.size(2)
    n_nodes = z.size(0)
    z = z.reshape(n_nodes, -1)
    n_dim = z.size(1)
    gamma = args[2]  # gamma / beta for the weight of ortho loss
    smooth_loss = _attn_s_smoothing(graph, *args)
    # mask
    # ones = [torch.ones(dim, dim, device=z.device) for _ in range(n_dim // dim)]
    # mask = torch.block_diag(*ones)
    # ortho_loss = torch.sum((torch.mm(z.T, z) * mask / n_nodes - torch.eye(n_dim, device=z.device)) ** 2)
    ortho_loss = torch.sum((torch.mm(z.T, z) / n_nodes - torch.eye(n_dim, device=z.device)) ** 2)
    return smooth_loss + ortho_loss * gamma


def _attn_s_smoothing_ortho_bak(graph: dgl.DGLGraph, *args) -> torch.Tensor:
    z: torch.Tensor = graph.ndata['ft']
    dim = z.size(2)
    n_nodes = z.size(0)
    z = z.reshape(n_nodes, -1)
    n_dim = z.size(1)
    gamma = args[2]  # gamma / beta for the weight of ortho loss
    smooth_loss = _attn_s_smoothing(graph, *args)
    # mask
    ones = [torch.ones(dim, dim, device=z.device) for _ in range(n_dim // dim)]
    mask = torch.block_diag(*ones)
    ortho_loss = torch.sum(
        (torch.mm(z.T, z) * mask / n_nodes - torch.eye(n_dim, device=z.device)) ** 2)
    # ortho_loss = torch.sum((torch.mm(z.T, z) / n_nodes - torch.eye(n_dim, device=z.device)) ** 2)
    return smooth_loss + ortho_loss * gamma
